function countToTen() {
  let counter = 0
  while (counter <= 10) {
      console.log(`The counter is ${counter}`)
      counter++
  }
  return counter
}

function countingFurther(number) {
  counter = 0
  while (counter < number) {
      console.log(`The counter is ${counter}`)
      counter++
  }
  return counter
}